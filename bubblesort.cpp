#include <bits/stdc++.h>
using namespace std; 


void bubblesort(int arr[],int n){

    for(int i=n-1;i>=0;i--){
        for(int j=0;j<=i-1;j++){
            if(arr[j]>arr[j+1]){
                swap(arr[j],arr[j+1]);
            }
        }
    }
}

int main (){
    int n ;
    cout<<"enter the n : ";
    cin>> n ;
    int arr[n];
    cout<<"enter the arr elements : ";
    for(int i =0; i<n;i++){
        cin>>arr[i];
    }
    bubblesort(arr, n );
    cout<<"the sorted arr is : ";
    for(int i=0;i<n;i++){
        cout<<arr[i]<<" ";
    }
}